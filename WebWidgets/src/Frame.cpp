#include "Poco/WebWidgets/Frame.h"


namespace Poco {
namespace WebWidgets {


Frame::Frame():
	View(typeid(Frame)),
	_pChild(),
	_title(),
	_collapsible(false)
{
}


Frame::Frame(const std::string& name):
	View(name, typeid(Frame)),
	_pChild(),
	_title(),
	_collapsible(false)
{
}


Frame::Frame(const std::string& name, const std::string& title):
	View(name, typeid(Frame)),
	_pChild(),
	_title(title),
	_collapsible(false)
{
}


Frame::Frame(View::Ptr pChild):
	View(typeid(Frame)),
	_pChild(),
	_title(),
	_collapsible(false)
{
	setChild(pChild);
}


Frame::Frame(const std::string& name, View::Ptr pChild):
	View(name, typeid(Frame)),
	_pChild(),
	_title(),
	_collapsible(false)
{
	setChild(pChild);
}


Frame::Frame(const std::string& name, const std::string& title, View::Ptr pChild):
	View(name, typeid(Frame)),
	_pChild(),
	_title(title),
	_collapsible(false)
{
	setChild(pChild);
}


Frame::Frame(const std::type_info& type):
	View(type),
	_pChild(),
	_title(),
	_collapsible(false)
{
}


Frame::Frame(const std::string& name, const std::type_info& type):
	View(name, type),
	_pChild(),
	_title(),
	_collapsible(false)
{
}


Frame::Frame(View::Ptr pChild, const std::type_info& type):
	View(type),
	_pChild(),
	_title(),
	_collapsible(false)
{
	setChild(pChild);
}


Frame::Frame(const std::string& name, View::Ptr pChild, const std::type_info& type):
	View(name, typeid(Frame)),
	_pChild(),
	_title(),
	_collapsible(false)
{
	setChild(pChild);
}


Frame::~Frame()
{
}


void Frame::setChild(View::Ptr pChild)
{
	if (_pChild)
		rejectChild(_pChild);
	if (pChild)
		adoptChild(pChild);
	_pChild = pChild;
}


View::Ptr Frame::findChild(const std::string& name) const
{
	if (_pChild && name == _pChild->getName())
		return _pChild;

	return View::Ptr();
}


} } // namespace Poco::WebWidgets
